#include "libft.h"
#include "fillit.h"

int		ft_sqrt_ceil(int nb)
{
	int pow;

	pow = 1;
	if (nb <= 0)
		return (0);
	while (pow * pow < nb)
	{
		pow++;
	}
	return (pow);
}
